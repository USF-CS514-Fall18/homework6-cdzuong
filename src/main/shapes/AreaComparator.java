package main.shapes;

import java.util.Comparator;

/** Area Comparator - compares two Shape2D variables  based on the area */
public class AreaComparator implements Comparator<Shape2D> {

	/** Compares main.shapes based on the area
	 *
	 * @param shape1 first shape
	 * @param shape2 second shape
	 * @return positive value if area of shape1 is greater than area of shape 2,
	 * 		   negative value if area of shape1 is less than area of shape 2,
	 * 		   and 0 if they are equal.
	 */
	@Override
	public int compare(Shape2D shape1, Shape2D shape2) {
		if(shape1.area() > shape2.area()) {
			return 1;
		}
		else if(shape1.area() == shape2.area()) {
			return 0;
		}
		else {
			return -1;
		}
	}

}
