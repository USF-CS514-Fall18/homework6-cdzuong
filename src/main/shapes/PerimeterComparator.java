package main.shapes;

import java.util.Comparator;

/**
 * Perimeter Comparator - compares two Shape2D variables based on the perimeter
 */
public class PerimeterComparator implements Comparator<Shape2D> {

    /**
     * Compares main.shapes based on the perimeter
     *
     * @param shape1 first shape
     * @param shape2 second shape
     * @return positive value if perimeter of shape1 is greater than perimeter of shape 2,
     * negative value if perimeter of shape1 is less than perimeter of shape 2,
     * and 0 if they perimeters are equal.
     */
    @Override
    public int compare(Shape2D shape1, Shape2D shape2) {
        if (shape1.perimeter() > shape2.perimeter()) {
            return 1;
        } else if (shape1.perimeter() == shape2.perimeter()) {
            return 0;
        } else {
            return -1;
        }
    }
}
